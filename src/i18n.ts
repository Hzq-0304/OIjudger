type MessageKey = keyof typeof messages.en;

export function t(key: MessageKey, params: Record<string, string | number> = {}): string {
  let value: string = messages[getLocale()][key] ?? messages.en[key];
  for (const [name, replacement] of Object.entries(params)) {
    value = value.replace(new RegExp(`\\{${name}\\}`, 'gu'), String(replacement));
  }
  return value;
}

export function getLocale(): Locale {
  const setting = vscode.workspace.getConfiguration('oijudger').get<LanguageSetting>('language', 'auto');
  if (setting === 'zh' || setting === 'en') {
    return setting;
  }
  return vscode.env.language.toLowerCase().startsWith('zh') ? 'zh' : 'en';
}
